const DEFAULT_LANG = 'en';

function getLang() {
  return localStorage.getItem('gs_lang') || DEFAULT_LANG;
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) lang = DEFAULT_LANG;
  localStorage.setItem('gs_lang', lang);
  applyLang(lang);
  window.location.reload();
}

function T(key) {
  const lang = getLang();
  return TRANSLATIONS[lang]?.[key] || TRANSLATIONS[DEFAULT_LANG]?.[key] || key;
}

function TF(key, values = {}) {
  let text = T(key);
  for (const [name, value] of Object.entries(values)) {
    text = text.replace(new RegExp(`{${name}}`, 'g'), value);
  }
  return text;
}

function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = TRANSLATIONS[lang]?.[key] || TRANSLATIONS[DEFAULT_LANG]?.[key];
    if (!val) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => applyLang(getLang()));

window.T = T;
window.setLang = setLang;
window.getLang = getLang;
